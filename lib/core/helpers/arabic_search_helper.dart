/// ArabicSearchHelper
/// 
/// ملف مساعد لتنظيف وتوحيد الحروف العربية، وهو مصمم خصيصاً لحل مشكلة
/// البحث في نصوص القرآن الكريم المكتوبة بالرسم العثماني.
/// 
/// المشكلة:
/// نصوص القرآن الكريم في التطبيق تحتوي على تشكيل كامل (فتحة، ضمة، كسرة، الخ)
/// وعلامات الوقف الخاصة بالمصحف (مثل ۖ، ۗ، ۘ) وبعض الحروف المكتوبة بطريقة 
/// الرسم العثماني (مثل الألف الخنجرية). عندما يقوم المستخدم بالبحث، فإنه عادة 
/// يكتب نصاً مجرداً بدون أي تشكيل (مثلاً "الحمد لله"). البحث العادي (contains)
/// سيفشل في المطابقة لأن الحروف غير متطابقة.
/// 
/// الحل:
/// نقوم بتجريد كل من نص الآية الكريمة، والنص الذي أدخله المستخدم من جميع
/// الحركات والرموز، ونوحد أشكال الألف والياء لنصل إلى نص أساسي (Normalized)
/// يمكننا إجراء المطابقة عليه بسهولة وبدقة عالية.
class ArabicSearchHelper {
  static String normalize(String text) {
    if (text.isEmpty) return text;

    String normalized = text;

    // 1. إزالة التشكيل (الحركات) وعلامات الوقف وعلامات المصحف الخاصة
    // القائمة تشمل:
    // الفتحة، الضمة، الكسرة، التنوين بأنواعه، الشدة، السكون،
    // علامات الوقف (م، لا، ج، صلي، قلي، الخ)،
    // ومختلف العلامات القرآنية الموجودة في كود اليونيكود للغة العربية (0600 - 06FF).
    normalized = normalized.replaceAll(RegExp(r'[\u0610-\u061A\u064B-\u065F\u0670\u06D6-\u06ED]'), '');

    // 2. توحيد أشكال حرف الألف
    // نحول (أ، إ، آ، ٱ) إلى ألف مجردة (ا)
    normalized = normalized.replaceAll(RegExp(r'[أإآٱ]'), 'ا');

    // 3. توحيد أشكال الياء والألف المقصورة
    // نحول الألف المقصورة (ى) إلى ياء (ي) للبحث بشكل أسهل
    normalized = normalized.replaceAll('ى', 'ي');

    // 4. توحيد التاء المربوطة والهاء (اختياري، لكنه يحسن نتائج البحث)
    // الكثير من المستخدمين يكتب "الصلوه" بدلاً من "الصلاة"، لكن سنكتفي بتوحيد الأساسيات هنا للحفاظ على معنى الكلمة.
    // normalized = normalized.replaceAll('ة', 'ه');

    // 5. إزالة التطويل (ـ)
    normalized = normalized.replaceAll('ـ', '');

    // إرجاع النص بعد إزالة المسافات الزائدة من الأطراف
    return normalized.trim();
  }
}
